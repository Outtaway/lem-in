#include "libft.h"

int		ft_putstrn(char const *s, int n)
{
	size_t i;

	if (!s)
		return (0);
	i = 0;
	while (s[i] && (int)i < n)
	{
		ft_putchar(s[i]);
		++i;
	}
	return (n);
}
