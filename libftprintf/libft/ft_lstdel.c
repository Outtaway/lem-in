#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	size_t i;

	if (!*alst || !alst)
		return ;
	i = 0;
	while ((*alst))
	{
		del((*alst)->content, (*alst)->content_size);
		free(*alst);
		*alst = (*alst)->next;
	}
	*alst = NULL;
}
